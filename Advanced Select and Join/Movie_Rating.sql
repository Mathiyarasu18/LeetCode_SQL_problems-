FROM MOvieRating m1
JOIN Users u ON m1.user_id = u.user_id
GROUP BY m1.user_id
ORDER BY COUNT(rating) DESC,name LIMIT 1)
UNION ALL
(SELECT title AS results
FROM MovieRating m1
JOIN Movies m2 ON m1.movie_id = m2.movie_id
WHERE YEAR(created_at) ='2020' AND MONTH(created_at) ='02'
GROUP BY title
ORDER BY AVG(m1.rating) DESC,
title LIMIT 1);

